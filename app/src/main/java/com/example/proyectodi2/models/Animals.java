package com.example.proyectodi2.models;

public class Animals {
    private String id;
    private String title; // Cambiado a 'titulo'
    private String description; // Cambiado a 'descripcion'
    private String imageUrl; // Cambiado a 'imagen'

    public Animals(){

    }

    public Animals(String id, String title, String description, String imageUrl) {
        this.id = id;
        this.title = title;
        this.description = description;
        this.imageUrl = imageUrl;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }
}
